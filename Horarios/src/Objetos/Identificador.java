package Objetos;

/**
 *
 * @author Familia
 */
public class Identificador {
    
    String hora;
    int dia;

    public Identificador(String hora, int dia) {
        this.hora = hora;
        this.dia = dia;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    @Override
    public String toString() {
        return dia+" "+hora;
    }
    
    
}
